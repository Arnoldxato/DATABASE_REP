CREATE TABLE OFFICERS(
    OFFICERS_ID INT,
    FIRST_NAME VARCHAR(45),
    LAST_NAME VARCHAR(45),
    OFFICER_RANK VARCHAR(45),
    BADGE_NO VARCHAR(45),
    PRIMARY KEY(OFFICERS_ID)
);

CREATE TABLE CRIMES(
    CRIME_ID INT,
    OFFICERS_ID INT,
    NATIONALITY VARCHAR(45),
    CRIME_TYPE VARCHAR(45),
    CRIME_DATE VARCHAR(45),
    CRIME_TIME VARCHAR(45),
    CRIME_LOCATION VARCHAR(20),
    PRIMARY KEY(CRIME_ID),
    Foreign Key (OFFICERS_ID) REFERENCES OFFICERS (OFFICERS_ID)
);
CREATE TABLE OFFENDERS(
    OFFENDER_ID INT,
    CRIME_ID INT,
    OFFENDERS_NAME VARCHAR(20),
    GENDER VARCHAR(45),
    NATIONALITY VARCHAR(45),
    DOB VARCHAR(20),
    PRIMARY KEY(OFFENDER_ID),
    Foreign Key (CRIME_ID) REFERENCES CRIMES (CRIME_ID)
);
CREATE TABLE ARREST(
    ARREST_ID INT,
    CRIME_ID INT,
    CHARGES VARCHAR(20),
    PRIMARY KEY(ARREST_ID),
    Foreign Key (CRIME_ID) REFERENCES CRIMES(CRIME_ID)
);
CREATE TABLE EVIDENCE(
    EVIDENCE_ID INT,
    EVIDENCE_TYPE VARCHAR(20),
    DESCRIPTIONS TEXT,
    PRIMARY KEY(EVIDENCE_ID)
);
CREATE TABLE ANALYSIS(
    ANALYSIS_ID INT,
    EVIDENCE_ID INT,
    ANALYSIS_TYPE VARCHAR(20),
    ANALYSIS_RESULT TEXT,
    PRIMARY KEY(ANALYSIS_ID),
    Foreign Key (EVIDENCE_ID) REFERENCES EVIDENCE  (EVIDENCE_ID)
);

